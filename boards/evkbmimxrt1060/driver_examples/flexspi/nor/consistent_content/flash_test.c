#define READ_SIZE        (256)

#define PRINT_CHUNK_SIZE (0x1000)

void flash_test(void)
{
    static uint8_t buffer_ipc[READ_SIZE];
    static uint8_t buffer_raw[READ_SIZE];

    while (1)
    {

        bool compare             = true;

        uint32_t bad_region[256] = {0};
        uint32_t region_count    = 0;

        // check flash contents
        for (uint32_t idx = 0x0000; idx < 0x20000; idx += READ_SIZE)
        {
            flash_read(idx, buffer_raw, READ_SIZE);
            flash_read_ipc(idx, buffer_ipc, READ_SIZE);

            bool bad = false;

            if ((idx % PRINT_CHUNK_SIZE) == 0)
            {
                PRINTF("TESTING %08X\r\n", FLASH_DEVICE_BASE_ADDR + idx);
            }

            for (uint32_t i = 0; i < READ_SIZE; i++)
            {
                uint32_t address = FLASH_DEVICE_BASE_ADDR + idx + i;
                if (buffer_ipc[i] != buffer_raw[i])
                {
                    PRINTF(
                        "DIFF (Raw) - %08X - %08X %08X\r\n", address,
                        buffer_ipc[i], buffer_raw[i]
                    );
                    compare = false;
                    bad     = true;
                }
            }

            if (bad)
            {
                bad_region[region_count] = idx;
                region_count++;
            }

            if (!compare)
            {
                // break;
            }
        }

        PRINTF("MEMORY TEST: %d\r\n", compare ? 1 : 0);

        for (uint32_t j = 0; j < 256; j++)
        {
            if (bad_region[j] != 0)
            {
                uint32_t idx = bad_region[j];

                PRINTF("CHECK %08X\r\n", FLASH_DEVICE_BASE_ADDR + idx);

                flash_read(idx, buffer_raw, 256);
                flash_read_ipc(idx, buffer_ipc, 256);

                for (uint32_t i = 0; i < 256; i++)
                {
                    uint32_t address = FLASH_DEVICE_BASE_ADDR + idx + i;
                    if (buffer_ipc[i] != buffer_raw[i])
                    {
                        PRINTF(
                            "DIFF (Raw) - %08X - %08X %08X\r\n", address,
                            buffer_ipc[i], buffer_raw[i]
                        );
                    }
                }
            }
            else
            {
                break;
            }
        }

        SDK_DelayAtLeastUs(1000 * 1000 * 2, BOARD_BOOTCLOCKRUN_CORE_CLOCK);

        break;
    }

    while (1)
    {
        __NOP();
    }

    for (uint16_t i = 0; i < 256; i++)
    {
        PRINTF("CRC: %d - %x\r\n", i, max_crc8_lookup);
    }
}
